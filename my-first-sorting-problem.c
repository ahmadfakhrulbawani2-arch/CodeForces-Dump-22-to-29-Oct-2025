int main(void) {
    int t;
    scanf("%d", &t);
    while(t--) {
        int a, b;
        scanf("%d %d", &a, &b);
        if(a > b) printf("%d %d", b, a);
        else printf("%d %d", a, b);
        printf("\n");
    }
    return 0;
}


